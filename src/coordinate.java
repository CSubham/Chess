public class coordinate {

    private int r;
    private int c;

    public coordinate(int r, int c) {
        this.r = r;
        this.c = c;
    }

    public coordinate( ) {}

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

}
